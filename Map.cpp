Map::Map() {
	
	dirt  = Texture_Manager::Load_Texture("Assets/Tile_Map/dirt.png");
	grass = Texture_Manager::Load_Texture("Assets/Tile_Map/grass.png");
	water = Texture_Manager::Load_Texture("Assets/Tile_Map/water.png");

	if (!dirt) {
		std::cout << "Failed to load dirt texture!" << std::endl;
	}
	if (!grass) {
		std::cout << "Failed to load grass texture!" << std::endl;
	}
	if (!water) {
		std::cout << "Failed to load water texture!" << std::endl;
	}
	else {
		std::cout << "Map loaded correctly" << std::endl;
	}

	Load_Map(Lvl_1);

	// SRC Rect
	src_rect.x = 0;
	src_rect.y = 0;

	src_rect.w = 32;
	src_rect.h = 32;

	// DEST Rect
	dest_rect.w = 32;
	dest_rect.h = 32;

	dest_rect.x = 0;
	dest_rect.y = 0;

}

Map::~Map() {
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(dirt);
	SDL_DestroyTexture(water);

}

void Map::Load_Map(int arr[31][57]) {

	for (int x = 0; x < 31; x++) {
		for (int y = 0; y < 57; y++) {
			map[x][y] = arr[x][y];
		}
	}
}

void Map::Draw_Map() {

	int type = 0;

	for (int x = 0; x < 31; x++) {
		for (int y = 0; y < 57; y++) {

			type = map[x][y];

			const int TILE_SIZE = 32;

			dest_rect.x = y * TILE_SIZE;
			dest_rect.y = x * TILE_SIZE;
			dest_rect.w = TILE_SIZE;
			dest_rect.h = TILE_SIZE;


			switch (type)
			{

			case 0:
				Texture_Manager::Draw(water, src_rect, dest_rect);
				break;
			
			case 1:
				Texture_Manager::Draw(grass, src_rect, dest_rect);
				break;
			
			case 2:
				Texture_Manager::Draw(dirt, src_rect, dest_rect);
				break;

			default:
				break;
			}
		}
	}
}